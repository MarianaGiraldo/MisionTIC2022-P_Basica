    private void btnGuardarPasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPasActionPerformed
        try{
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String tipoDocumento = cbTipoDoc.getSelectedItem().toString();
            String numDocumento = txtNumDoc.getText();
            String sexo = cbSexo.getSelectedItem().toString();
            String contacto = txtContacto.getText();
            String email = txtEmail.getText();
            
            if(nombre.equals("") || apellido.equals("") || numDocumento.equals("")|| contacto.equals("") || email.equals("")){
                JOptionPane.showMessageDialog(this, "Fill all fields");
            }else{
                this.pasajero.setNombre(nombre);this.pasajero.setApellido(apellido);this.pasajero.setTipoDocumento(tipoDocumento);
                this.pasajero.setNumDocumento(numDocumento);this.pasajero.setSexo(sexo);this.pasajero.setContacto(contacto);this.pasajero.setEmail(email);
                
                int resp = JOptionPane.showConfirmDialog(this, "El registro del pasajero se ha guardado", "Registro", OK_CANCEL_OPTION);
                switch(resp){
                    case 0->{this.dispose();}
                    case 1->{this.pasajero= new PasajeroAdulto();}
                }
            }
        }catch(HeadlessException e){
            JOptionPane.showMessageDialog(this, "Error");
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_btnGuardarPasActionPerformed

